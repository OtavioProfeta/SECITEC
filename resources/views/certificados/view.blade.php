<script>
    // Evento de input e a tabela
    var input = document.getElementById('searchInput');
    var table = document.querySelector('.table');

    // Adiciona um ouvinte de eventos ao input para detectar mudan√ßas de entrada
    input.addEventListener('input', function () {
        var searchText = input.value.toUpperCase();

        // Obt√©m todas as linhas da tabela
        var rows = table.querySelectorAll('tbody tr');

        // Itera sobre as linhas e verifica se o texto de pesquisa corresponde ao nome do usu√°rio ou nome do evento
        rows.forEach(function (row) {
            var nomeUsuario = row.querySelector('#nome-usuario').textContent.toUpperCase();

            if (nomeUsuario.includes(searchText)) {
                row.style.display = 'table-row';
            } else {
                row.style.display = 'none';
            }
        });
    });
</script>
