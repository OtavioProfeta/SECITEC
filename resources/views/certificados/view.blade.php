<script>
    function selecionarSecao(secaoIdx){
        const secoes = Array.from(document.querySelectorAll("#sobre-section"));
        secoes.forEach(secao => {
            secao.classList.remove('show');
        });

        secoes[secaoIdx].classList.add('show');
    }
